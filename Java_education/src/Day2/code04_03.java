package Day2;

public class code04_03 {
    public static void main(String[] args){
        int n1 = 1;
        int n2 = 2;

        System.out.println(n1+n2);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2);
    }
}
// n2가 실수기에 0이라는 수가 나왔다. 파이썬의 //가 적용된 것이다.
// 만약 단순하게 //가 아닌 나누기를 적용하고 싶다면 n2를 double로 작성해서 실수로 바꿔야 한다.
// 정수와 실수의 계산 결과는 실수이다.