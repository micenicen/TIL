package Day2;

public class code03_11 {
    public static void main(String[] args){
        int n1 = 100, n2 = 200;
        System.out.print(n1 == n2);
        System.out.println(n1 != n2);

        System.out.print(n1 > n2);
        System.out.println(n1 < n2);

        System.out.print(n1 >= n2);
        System.out.println(n1 <= n2);
    }
}
// 이러한 방식으로 true와 false를 나눌 수 있다.
