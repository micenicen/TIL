package Day2;

public class code03_01 {
    public static void main(String[] args) {
        int n1, n2, res;
        n1 = 5;
        n2 = 3;
        res = n1 + n2;
        System.out.println((res));
    }
}
// 연산자는 기본적으로 더하기 +, 빼기 -, 곱하기 *, 나누기 /, 나머지 % 로 구분된다.
// 여기서 나누기가 특색이 있는데 나누기의 슬래시는 정수끼리 나누면 몫만 나오는 것이 특징이다.

