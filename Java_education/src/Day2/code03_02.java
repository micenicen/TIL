package Day2;

public class code03_02 {
    public static void main(String[] args) {
        int n1, n2, res;
        n1 = 5;
        n2 = 3;
        res = n1 + n2;
        System.out.println((res));
        res = n1 - n2;
        System.out.println((res));
        res = n1 * n2;
        System.out.println((res));
        res = n1 / n2;
        System.out.println((res));
        res = n1 % n2;
        System.out.println((res));
    }
}
// 4번째 몫은 1이 나왔다. 만약에 실수인 숫자로 대입하면 값은 몫이 아닌 실수가 나오게 된다.
