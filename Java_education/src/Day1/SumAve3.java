package Day1;

public class SumAve3 {
    public static void main(String[] args){
        int x = 63;                                                          // x는 정수
        int y = 18;                                                          // y는 정수


        System.out.println("x의 값은 " + x + "입니다.");                  // x의 값을 출력하였다.
        System.out.println("y의 값은 " + y + "입니다.");                  // y의 값을 출력하였다.
        System.out.println("합계는 " +(x + y) + "입니다.");               // 합을 출력하였다.
        System.out.println("평균은 " + (x + y)/2 + "입니다.");            // 평균값을 출력하였다.

    }
}

