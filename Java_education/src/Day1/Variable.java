package Day1;

public class Variable {
    public static void main(String[] args){
        int x;                                  // x는 정수라고 선언하였다.

        x = 63;                                 // 정수 x를 63으로 선언하였다.

        System.out.println(x);                  // x의 값을 출력하였다.

    }
}
// 변수를 지정하고 변수를 선언하고 선언된 변수를 출력하는 기본 절차이다.
