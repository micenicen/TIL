package Day1;

public class Hello {
    public static void main(String[] args){                // 이 부분은 상용문구로 기억해두자.
        System.out.println("처음 작성하는 Java 프로그램.");    // 여기와 아랫부분은 출력부이다.
        System.out.println("화면으로 출력하고 있습니다.");
    }
}
//선언은 public, private, protect, 아무것도 없는것 등으로 4개가 존재한다.
