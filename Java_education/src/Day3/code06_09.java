package Day3;

public class code06_09 {
    public static void main(String[] args) {

        for (int i=0 ; i<3 ; i++) {
            for (int k=0 ; k<2 ; k++){
                System.out.println("i : " + i + " k : "+ k);
            }
        }

    }
}
